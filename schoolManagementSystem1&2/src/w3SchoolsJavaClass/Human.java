package w3SchoolsJavaClass;

public class Human {
	private Ear ear;
	private Nose nose;
	private Eye eye;
	private Lip lip;
	private Leg leg;
	private Hand hand;
	public Ear getEar() {
		return ear;
	}
	public void setEar(Ear ear) {
		this.ear = ear;
	}
	public Nose getNose() {
		return nose;
	}
	public void setNose(Nose nose) {
		this.nose = nose;
	}
	public Eye getEye() {
		return eye;
	}
	public void setEye(Eye eye) {
		this.eye = eye;
	}
	public Lip getLip() {
		return lip;
	}
	public void setLip(Lip lip) {
		this.lip = lip;
	}
	public Leg getLeg() {
		return leg;
	}
	public void setLeg(Leg leg) {
		this.leg = leg;
	}
	public Hand getHand() {
		return hand;
	}
	public void setHand(Hand hand) {
		this.hand = hand;
	}
	
	
	

	
	
	

}
